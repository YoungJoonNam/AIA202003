package ch_operator;

public class PrefixOp {

	public static void main(String[] args) {
		
		int nNum1 = 7;					// nNum1 = 7
		int nNum2 = ++nNum1;		// nNum2 = 8, nNum1=8
		int nNum3 = --nNum1;
		
		System.out.println(++nNum1); 
		System.out.println(nNum2);
		System.out.println(nNum3);
		System.out.println(--nNum2);

	}

}
